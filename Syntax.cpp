// #include <iostream> is a header file library which work with input and output objects such as cout.  
// Header file provides functionality to C++ Programs.  
#include <iostream>
// using namespace means that we can used names for objects and variables from standard library.
using namespace std;
// int main() is a function 
int main(){
    // cout is an object.
    // << is an insertion operator to print a text. 
    cout << "Hello World";
    // return 0 ends the main function
    return 0;
}

// Omitting namespace :- instead of using (using namespace std) u can used (std ::) operator for some objects. 
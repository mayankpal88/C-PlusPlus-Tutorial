#include <iostream>
using namespace std;
int main(){
    // Arthmetic operators
    int x = 12;
    int y = 10;
    cout << x+y << endl;
    cout << x-y << endl;
    cout << x*y << endl;
    cout << x%y << endl;
    cout << x/y << endl;
    cout << x++ << endl;
    cout << x-- << endl;
    cout << --x << endl;
    cout << ++x << endl;
    // Assignment Operators
    int m = 88;
    cout << m << endl ;
    // Comparison Operators
    int n = 72;
    int i = 11 ;
    cout << (n==i) << endl; 
    cout << (n!=i) << endl;
    cout << (n<i) << endl;
    cout << (n>=i) << endl;
    cout << (n>i) << endl;
    // Logical Operators
    int j = 88;
    int k = 20;
    cout << (j<12 && k>28) << endl;
    cout << (j<12 || k>28) << endl;

}
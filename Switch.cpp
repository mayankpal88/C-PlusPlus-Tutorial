#include <iostream>
using namespace std;
int main(){
    // Switch Function
    int day = 1;
switch (day) {
  case 1:
    cout << "Monday";
    // Break keyword is used to come out of the case if that true  
    break;
  case 2:
    cout << "Tuesday";
    break;
  case 3:
    cout << "Wednesday";
    break;
  case 4:
    cout << "Thursday";
    break;
  case 5:
    cout << "Friday";
    break;
  case 6:
    cout << "Saturday";
    break;
  case 7:
    cout << "Sunday";
    break;
}
} 
#include <iostream>
using namespace std;
int main(){
    // integer
    int myNum = 77;
    cout << myNum << endl;
    // double
    double mynum = 19.99;
    cout << mynum << endl;
    // String
    string name = "C++ Tutorial";
    cout << name << endl;
    // Boolean
    bool Boole = true;
    cout << Boole << endl;
    // Character
    char ch = 'A';
    cout << ch << endl;
    // Display Variables
    int myage = 38;
    cout << "I am " << myage << " old" << endl;
    // Add variables together
    int x = 5;
    int y = 7;
    cout << (x+y) << endl ;
    // Declaring multiple variables
    int m = 0 , n = 9 , o = 10;
    cout << (m+n+o) << endl;
    // Indetifiers
    int minutesPerHr = 60;
    cout << minutesPerHr;
    // Constants
    const int n1 = 15;
    const float PI = 3.14;
    cout << n1 << endl;
    cout << PI << endl;
}